    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String buscarpor;
        buscarpor = jTextField1.getText();
        ControllerCita ex = new ControllerCita();
        LinkedList<Cita> le = ex.buscarId(buscarpor);      
        for (int i = 0; i < le.size(); i++) {
            jTextField3.setText(le.get(i).getHorarioCita());
            jTextField2.setText(le.get(i).getMotivaCita());
            jTextField5.setText(le.get(i).getFechaCita());
            jTextField4.setText(le.get(i).getGravedadCita());
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

