    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String buscarpor;
        buscarpor = jTextField1.getText();
        ControllerMascota obD = new ControllerMascota();

        HashMap<Mascota, Imagen> ld = obD.buscarMascotaCodigo(buscarpor);

        jTextField2.setText(ld.entrySet().iterator().next().getKey().getNombreM());
        jTextField3.setText(ld.entrySet().iterator().next().getKey().getEdadM());
        jTextField4.setText(ld.entrySet().iterator().next().getKey().getEspecieM());
        jTextField5.setText(ld.entrySet().iterator().next().getKey().getSubespecieM());
        jLabel8.setIcon(new javax.swing.ImageIcon((Image) ld.entrySet().iterator().next().getValue().getImagen()));

    }//GEN-LAST:event_jButton3ActionPerformed

